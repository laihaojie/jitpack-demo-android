package com.example.shengqi.recyclerview;

public class itemObject {
    private String name;
    private int imageId;

    public itemObject(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
}
